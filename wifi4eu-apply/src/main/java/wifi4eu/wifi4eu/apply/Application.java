package wifi4eu.wifi4eu.apply;

public class Application {

    public String redis_id;
    public String r;
    public String u;
    public String m;
    public String ip;
    public String data;

    public Application(String redis_id, String r, String u, String m, String ip, String data) {

        this.redis_id = redis_id;
        this.r = r;
        this.u = u;
        this.m = m;
        this.ip = ip;
        this.data = data;
    }
}
