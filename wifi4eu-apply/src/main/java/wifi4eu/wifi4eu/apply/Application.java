package wifi4eu.wifi4eu.apply;

public class Application {

    public String id;
    public String r;
    public String u;
    public String m;
    public String ip;
    public String ecas;
    public String data;

    public Application(String id, String r, String u, String m, String ip, String ecas, String data) {

        this.id = id;
        this.r = r;
        this.u = u;
        this.m = m;
        this.ip = ip;
        this.ecas = ecas;
        this.data = data;
    }
}
